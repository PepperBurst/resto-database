                                    + txtCusInfoLname.getText()  + "', "
                                    + "cusUsername='"
                                    + txtCusInfoUsername.getText() + "', "
                                    + "cusPassword='"
                                    + txtCusInfoPassword.getText() + "', "
                                    + "cusSex='"
                                    + txtCusInfoSex.getText() + "'"
                                    + "where cusId='" + currCustomer.getID()
                                    + "'";
